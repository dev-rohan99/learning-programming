# numeric data type

age = 25 # int data type
age2 = 25.5 # float data type
complexNumber = 20 + 15j # complex number data type

print(type(age))
print(type(age2))
print(type(complexNumber))

# string data type

name = "Rohan Mostofa abir"
print(type(name))

# sequence data type

# => 01 list data type

city = ["Khulna", "Dhaka", "Rongpur"]
print(city)
print(type(city))

city = ["Khulna", "Dhaka", "Rongpur", "Khulna", "Dhaka"]
print(city)

alphabets = ["A", "C", "K", "N", "O"]
print(alphabets[2])

# tuple data table
details = (
    "Rohan Mostofa",
    22,
    "Narail"
)
print(details)

details2 = (
    "Rohan Mostofa",
    22,
    22,
    "Narail"
    "Narail"
)
print(details2)
print(details2[2])

# range data type
numbers = range(1, 10)
numbers2 = range(0, 20, 3)
print(type(numbers))
print(*numbers)
print(*numbers2)

# boolean datatype

user = True
print(type(user))

if user:
    print(user)
else:
    print(user)

# none datatype

taka = None
print(taka)
print(type(taka))

